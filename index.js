class Translation {
    constructor() {
        this.input = document.querySelector("#input-text");
        this.selected = null;  
        this.result = document.querySelector(".result");
        this.p = document.querySelector("#translated-text");
        this.button = document.querySelector("#translate-btn");
        this.fromLang = document.querySelector("#from-lang");
        this.toLang = document.querySelector("#to-lang");  
        this.addEventListener();
    }

    addEventListener() {
        this.button.addEventListener("click", () => {
            this.trim();
        });
    }

    trim() {
        const fromLangValue = this.fromLang.value;
        const toLangValue = this.toLang.value;

        const inputValue = this.input.value.trim().toLowerCase();
        
        if (fromLangValue === 'en' && toLangValue === 'uz') {  
            if (translation[inputValue]) {
                this.selected = translation[inputValue];
                this.p.innerHTML = this.selected;
                this.result.innerHTML = ''; 
                this.result.appendChild(this.p);
            } else {
                this.p.innerHTML = "No translation found"; 
                this.result.innerHTML = '';
                this.result.appendChild(this.p);
            }
        } else if (fromLangValue === 'uz' && toLangValue === 'en') {
            if (translation1[inputValue]) {
                this.selected = translation1[inputValue];
                this.p.innerHTML = this.selected;
                this.result.innerHTML = ''; 
                this.result.appendChild(this.p);
            } else {
                this.p.innerHTML = "No translation found"; 
                this.result.innerHTML = '';
                this.result.appendChild(this.p);
            }
        } else {
            this.p.innerHTML = "Translation not available for selected languages";
            this.result.innerHTML = '';
            this.result.appendChild(this.p);
        }
    }
}

